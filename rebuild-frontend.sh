#!/bin/bash
# Пересборка фронтенда и запуск сервера.
# Без пересборки изменения в таблицах (подсказка «Вопросы») не появятся — сайт отдаёт файлы из Frontend/build.
# Запускайте из корня проекта: ./rebuild-frontend.sh

set -e
cd "$(dirname "$0")"
echo "Останавливаем порты 3000, 3001..."
npx kill-port 3000 3001 2>/dev/null || true
sleep 2
echo "Запуск полной сборки и сервера (npm run dev)..."
npm run dev
