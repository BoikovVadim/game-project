import { RawQuestion, shuffle, shuffleOptions, rnd } from './types';

type EY3 = [string, string, string]; // [question, year, eventName]
type PD = [string, string]; // [question, person]
type Q5 = [string, string, string, string, string];

function wrongYears(y: number): string[] {
  const offsets = shuffle([-30, -20, -15, -10, -5, 5, 10, 15, 20, 30]);
  const w: string[] = [];
  const used = new Set([y]);
  for (const o of offsets) {
    const v = y + o;
    if (v > 0 && !used.has(v)) { w.push(String(v)); used.add(v); if (w.length >= 3) break; }
  }
  while (w.length < 3) w.push(String(y + w.length * 7 + 3));
  return w;
}

function yearQs(t: string, d: EY3[]): RawQuestion[] {
  return d.map(([q, yr]) => shuffleOptions(t, q, yr, wrongYears(parseInt(yr))));
}

function whatQs(t: string, d: EY3[]): RawQuestion[] {
  return d.map(([, yr, name], i) => {
    const others = d.filter((_, j) => j !== i);
    const wrongs = shuffle(others).slice(0, 3).map(e => e[2]);
    return shuffleOptions(t, `Какое событие произошло в ${yr} году?`, name, wrongs);
  });
}

function whoQs(t: string, d: PD[], pool: string[]): RawQuestion[] {
  return d.map(([q, person]) => {
    const wrongs = shuffle(pool.filter(p => p !== person)).slice(0, 3);
    return shuffleOptions(t, q, person, wrongs);
  });
}

function q5s(t: string, d: Q5[]): RawQuestion[] {
  return d.map(([q, c, w1, w2, w3]) => shuffleOptions(t, q, c, [w1, w2, w3]));
}

function qaPoolQs(t: string, qa: [string, string][], pool: string[]): RawQuestion[] {
  return qa.map(([q, a]) => {
    const wrongs = shuffle(pool.filter(p => p !== a)).slice(0, 3);
    return shuffleOptions(t, q, a, wrongs);
  });
}

function parse3(s: string): EY3[] { return s.trim().split('\n').filter(l => l.trim()).map(l => l.split('|') as unknown as EY3); }
function parse2(s: string): PD[] { return s.trim().split('\n').filter(l => l.trim()).map(l => l.split('|') as unknown as PD); }
function parseQ5(s: string): Q5[] { return s.trim().split('\n').filter(l => l.trim()).map(l => l.split('|') as unknown as Q5); }

