export function generateGeo500(): RawQuestion[] {
  const items: RawQuestion[] = [];
  const allCaps = C.map(c => c.c);
  const allCountries = C.map(c => c.n);
  const allCur = [...new Set(C.map(c => c.cur))];
  const allLang = [...new Set(C.map(c => c.lang))];
  const allCont = [...new Set(C.map(c => c.cont))];

  for (const c of C) {
    items.push(shuffleOptions('capitals', `Какая столица у страны ${c.n}?`, c.c, pick3(allCaps, c.c)));
    items.push(shuffleOptions('capitals', `${c.c} — столица какой страны?`, c.n, pick3(allCountries, c.n)));
    items.push(shuffleOptions('capitals', `В какой стране столица — ${c.c}?`, c.n, pick3(allCountries, c.n)));
    items.push(shuffleOptions('capitals', `Назовите столицу: ${c.n}`, c.c, pick3(allCaps, c.c)));

    items.push(shuffleOptions('currencies', `Какая валюта в стране ${c.n}?`, c.cur, pick3(allCur, c.cur)));
    items.push(shuffleOptions('currencies', `${c.cur} — валюта какой страны?`, c.n, pick3(allCountries, c.n)));
    items.push(shuffleOptions('currencies', `В какой стране расплачиваются валютой ${c.cur}?`, c.n, pick3(allCountries, c.n)));

    items.push(shuffleOptions('countries_facts', `На каком континенте расположена ${c.n}?`, c.cont, pick3(allCont, c.cont)));
    items.push(shuffleOptions('countries_facts', `На каком языке говорят в стране ${c.n}?`, c.lang, pick3(allLang, c.lang)));
    items.push(shuffleOptions('countries_facts', `${c.lang} язык является официальным в?`, c.n, pick3(allCountries, c.n)));

    items.push(shuffleOptions('flags', `Какой стране принадлежит столица ${c.c}?`, c.n, pick3(allCountries, c.n)));
  }

  for (const [name, country, wrong] of LANDMARKS) {
    items.push(shuffleOptions('landmarks', `В какой стране находится ${name}?`, country, wrong));
    items.push(shuffleOptions('landmarks', `${name} — достопримечательность какой страны?`, country, wrong));
    items.push(shuffleOptions('landmarks', `Какая достопримечательность находится в стране ${country}?`, name,
      pick3(LANDMARKS.map(l => l[0]), name)));
    items.push(shuffleOptions('architecture', `В какой стране расположено сооружение ${name}?`, country, wrong));
    items.push(shuffleOptions('architecture', `${name} находится в?`, country, wrong));
    items.push(shuffleOptions('wonders_of_world', `Где находится ${name}?`, country, wrong));
  }

  const allMountains = MOUNTAINS.map(m => m[0]);
  const allMountCountries = [...new Set(MOUNTAINS.map(m => m[1]))];
  for (const [name, country, height, wrongH] of MOUNTAINS) {
    items.push(shuffleOptions('mountains', `В какой стране находится гора ${name}?`, country, pick3(allMountCountries, country)));
    items.push(shuffleOptions('mountains', `Какова высота горы ${name}?`, `${height} м`, wrongH.map(w => `${w} м`)));
    items.push(shuffleOptions('mountains', `Гора ${name} расположена в?`, country, pick3(allMountCountries, country)));
    items.push(shuffleOptions('mountains', `Какая гора находится в стране ${country}?`, name, pick3(allMountains, name)));
    items.push(shuffleOptions('geography_facts', `Высота горы ${name}?`, `${height} м`, wrongH.map(w => `${w} м`)));
  }

  const allRivers = RIVERS.map(r => r[0]);
  const allRiverConts = [...new Set(RIVERS.map(r => r[1]))];
  for (const [name, cont, len, wrongL] of RIVERS) {
    items.push(shuffleOptions('rivers', `На каком континенте протекает река ${name}?`, cont, pick3(allRiverConts, cont)));
    items.push(shuffleOptions('rivers', `Какова длина реки ${name}?`, `${len} км`, wrongL.map(w => `${w} км`)));
    items.push(shuffleOptions('rivers', `Река ${name} протекает в?`, cont, pick3(allRiverConts, cont)));
    items.push(shuffleOptions('rivers', `Какая река протекает в ${cont}?`, name, pick3(allRivers, name)));
    items.push(shuffleOptions('geography_facts', `Длина реки ${name}?`, `${len} км`, wrongL.map(w => `${w} км`)));
  }

  for (const [q, a, w] of OCEANS) items.push(shuffleOptions('oceans', q, a, w));
  for (const [q, a, w] of DESERTS) items.push(shuffleOptions('deserts', q, a, w));
  for (const [q, a, w] of VOLCANOES) items.push(shuffleOptions('volcanoes', q, a, w));
  for (const [q, a, w] of ISLANDS) items.push(shuffleOptions('islands', q, a, w));

  return items;
}
