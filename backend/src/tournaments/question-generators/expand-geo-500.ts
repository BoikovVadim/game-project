function pick3<T>(arr: T[], exclude: T): T[] {
  const pool = arr.filter(x => x !== exclude);
  const res: T[] = [];
  const used = new Set<number>();
  while (res.length < 3 && used.size < pool.length) {
    const i = rnd(0, pool.length - 1);
    if (!used.has(i)) { used.add(i); res.push(pool[i]!); }
  }
  return res;
}

