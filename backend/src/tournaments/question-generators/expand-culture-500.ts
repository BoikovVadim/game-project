function pick3(pool: string[], exclude: string): string[] {
  const f = pool.filter(x => x !== exclude);
  const a = [...f];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j]!, a[i]!];
  }
  return a.slice(0, 3);
}

